package refactoring;

import java.awt.Point;

public class Diamond extends GameObjects {


	public int yield;

	public Diamond(Point point,Boolean b) {
		super(point,b);
		this.yield = 1;
	}
	
	@Override
	public void domutate() {
		this.yield *= 2;

		System.out.println("Yield is now " + this.yield);
	}
}
